  <div
    class="max-w-full sm:px-4 xl:py-8 xl:max-w-[1240px] mx-auto flex flex-col space-y-4 md:space-y-8"
  >
    <GameFrame
      v-if="!isMobile"
      @click:login="onClickLogin"
      @click:register="onClickRegister"
    />
